  function getLang() {
    var stored = localStorage.getItem(STORAGE_KEY);
    return (stored === 'uz' || stored === 'ru') ? stored : 'ru';
  }

  function setLang(lang) {
    localStorage.setItem(STORAGE_KEY, lang);
    document.documentElement.lang = lang === 'uz' ? 'uz' : 'ru';
  }

  function applyLang(lang) {
    if (lang !== 'ru' && lang !== 'uz') lang = 'ru';
    setLang(lang);
    var L = t[lang];

    document.querySelectorAll('[data-i18n]').forEach(function (el) {
      var key = el.getAttribute('data-i18n');
      if (L[key] !== undefined) el.textContent = L[key];
    });

    document.querySelectorAll('[data-i18n-href]').forEach(function (el) {
      var key = el.getAttribute('data-i18n-href');
      if (L[key] !== undefined) el.href = L[key];
    });

    document.querySelectorAll('[data-i18n-placeholder]').forEach(function (el) {
      var key = el.getAttribute('data-i18n-placeholder');
      if (L[key] !== undefined) el.placeholder = L[key];
    });

    document.querySelectorAll('[data-i18n-html]').forEach(function (el) {
      var key = el.getAttribute('data-i18n-html');
      if (L[key] !== undefined) el.innerHTML = L[key];
    });

    var pageTitleKey = document.body.getAttribute('data-i18n-page-title');
    if (pageTitleKey && L[pageTitleKey]) document.title = L[pageTitleKey];

    document.querySelectorAll('.lang-link').forEach(function (a) {
      a.classList.remove('active');
      if (a.getAttribute('data-lang') === lang) a.classList.add('active');
    });

    document.dispatchEvent(new CustomEvent('langChange', { detail: { lang: lang } }));
  }

  document.addEventListener('DOMContentLoaded', function () {
    var lang = getLang();
    applyLang(lang);

    document.querySelectorAll('[data-lang]').forEach(function (btn) {
      btn.addEventListener('click', function (e) {
        e.preventDefault();
        var l = this.getAttribute('data-lang');
        if (l === 'ru' || l === 'uz') applyLang(l);
      });
    });
  });

  window.i18n = { getLang: getLang, setLang: setLang, applyLang: applyLang, t: t };
